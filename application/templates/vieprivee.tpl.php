        <h2>Respect de la vie privée</h2>
            <ul>
            <li><span style="font-weight: bold;">L'IPM s’engage à respecter la
            législation sur la protection de la vie privée en vigueur en Belgique</span>:
            les traitements de données à caractère personnel relatifs à ce site
            sont soumis à la loi du 8 décembre 1992 relative à la protection de la
            vie privée à l'égard des traitements de données à caractère personnel,
            modifiée par la loi du 11 décembre 1998 transposant la directive
            95/46/CE du 24 octobre 1995 du Parlement européen et du Conseil
            relative à la protection des personnes physiques à l'égard du
            traitement de données à caractère personnel et à la libre circulation
            de ces données, ainsi qu'aux arrêtés royaux qui s'y rapportent. En
            outre, l'IPM applique les dispositions de la Convention Collective du
            Travail No 81 relative à la protection de la vie privée des
            travailleurs à l'égard du contrôle des données de communication
            électroniques en réseau.Les textes légaux peuvent être consultés sur le
            site de la Commission de la protection de la vie privée (<a
            href="http://www.privacy.fgov.be/">http://www.privacy.fgov.be/</a>).</li>
            <li>Le terme «<span style="font-style: italic;">donnée à caractère
            personnel</span>» recouvre toutes les informations concernant une
            personne physique identifiée ou identifiable. Une personne est
            identifiable lorsqu’elle peut être identifiée, directement ou
            indirectement, notamment par référence à un numéro d’identification ou
            à un ou plusieurs éléments spécifiques, propres à son identité
            physique, physiologique, psychique, économique, culturelle ou sociale.
            Par exemple : le nom et prénom d’une personne, son adresse, son numéro
            de téléphone, son adresse e-mail, ses préférences, son origine raciale,
            son sexe, … L’adresse et le numéro de téléphone professionnel d’une
            personne sont également des données à caractère personnel. Enfin, une
            adresse TCP/IP peut aussi, dans certains cas, être considérée comme une
            donnée à caractère personnel (Ministère des Affaires économiques:
            Recommandations en matière de vie privée : <a
            href="http://mineco.fgov.be/information_society/enterprises/providers_internetguide/Providers1_fr-01.htm">http://mineco.fgov.be/information_society/enterprises/providers_internetguide/Providers1_fr-01.htm</a>).</li>
            <li>Par «traitement», on entend toute opération ou ensemble
            d'opérations effectuées ou non à l'aide de procédés automatisés et
            appliquées à des données à caractère personnel, telles que la collecte,
            l'enregistrement, l'organisation, la conservation, l'adaptation ou la
            modification, l'extraction, la consultation, l'utilisation, la
            communication par transmission, diffusion ou toute autre forme de mise
            à disposition, le rapprochement ou l'interconnexion, ainsi que le
            verrouillage, l'effacement ou la destruction de données à caractère
            personnel.</li>
            </ul>
            <h2>Informations et données collectées et méthodes de collecte</h2>
            Pour les <span style="font-weight: bold;">membres UCL</span>,
            l'authentification est réalisée par interrogation du système
            d'identification central et lors d'une inscription à une formation, les
            données à caractère personnel collectéessont les suivantes :<br />
            <ul>
            <li>l'identifiant global de connexion;</li>
            <li>le nom, le prénom;</li>
            <li>l'identifiant FGS;</li>
            <li>la faculté, le département, l'unité;</li>
            <li>l'adresse, le téléphone et l'adresse électronique professionnels.</li>
            </ul>
            Les <span style="font-weight: bold;">personnes externes</span> qui
            s'inscrivent à une formation doivent créer un compte via un formulaire
            et les données suivantes sont enregistrées :<br />
            <ul>
            <li>l'identifiant et le mot de passe de connexion créés
            spécifiquement pour pouvoir s'inscrire aux formations IPM;</li>
            <li>le nom, le prénom;</li>
            <li>l'institution d'origine;</li>
            <li>la faculté, le département, l'unité;</li>
            <li>l'adresse, le téléphone et l'adresse électronique professionnels.</li>
            </ul>
            <h2>Traitements des données à caractère personnel et finalités</h2>
            Les données enregistrées <span style="font-weight: bold;">seront
            utilisées de manière nominative uniquement pour assurer le
            fonctionnement du service ou à des fins de promotion des formations</span>.
            <br />
            En particulier :<br />
            <ol>
            <li>Chaque formateur IPM a accès à la liste nominative des personnes
            inscrites à sa formation à tout moment;</li>
            <li>Vous recevrez des messages promotionnels vous invitant à vous
            inscrire à des formations de l'IPM.</li>
            <li>Vous recevrez des invitations à participer à des enquêtes concernant 
            les formations que vous avez suivies à l'IPM.</li>
            </ol>
            Les données enregistrées <span style="font-weight: bold;">seront
            utilisées de manière non nominative et globale dans un but de reporting
            interne à l'UCL</span> avec le concours des Services de l'UCL
            concernés, et en respectant l'arrêté royal du 13 février 2001 portant
            exécution de la loi du 8 décembre 1992.<br />
            <br />
            <span style="font-weight: bold;">Vos données personnelles ne seront en
            aucun cas cédées ou transmises à des tiers</span>. Toutefois, nous nous
            réservons le <span style="font-weight: bold;">droit de divulguer des
            informations et/ou données à caractère personnel sur requête d'une
            autorité légale</span> en conformité des lois et réglementations en
            vigueur.<br />
